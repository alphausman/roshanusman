						<h3>Challenges & Delivery Gaps</h3>
						<p>Old age presents with some challenges that are required to be addressed by eldercare. The challenges are not limited to the health hence the solutions can not only be limited to healthcare. The conversation with that old man motivated me to do something for elders and I researched vastly on the eldercare models of developed nations and how is India lagging behind.
						That very conversation implanted the seed of ivh SeniorCare (www.ivhsenircare.com).  </p>
						<h3>Health and Healthcare</h3>
							  <p>Health is the biggest challenge for the elderly. Aging results in some irreversible changes in the body that represent themselves as strength loss, low digestive power, decreased vision, knee pains, and decreased precision. Healthcare is the most efficient aspect of the eldercare and most elders have the access to healthcare services. Healthcare at home has improved the efficiency of the healthcare services. Although home healthcare is well established in most of the metro cities, it still has to establish itself in tier 2 and tier 3 cities. Despite having a well-established healthcare segment in metro cities, the elderly are still dependent on either family or domestic help for timely medication because of ailing memory. Depending on domestic health for medical care often do more harm than good. Most of the domestic help workers are illiterate and fail to read the medicine names written in English, they often rely on the colour and shape of the medicines.
							  And it is not very difficult to assume that two medicines with the same colour and shape will confuse them. </p>

<p>People often hire domestic help as healthcare concierge due to lack of awareness and monetary concerns. However, they don’t realize one single mistake of the person responsible for drug adherence can cost them much more.
 Trained attendants are the need of the hour when elderly people mostly live alone. </p>
<h3>Loneliness & Depression</h3>
<p>Elderly loneliness and depression have presented itself as the new healthcare challenge in India. 
The fact that 21.9% elders suffer depression became the headline of many distinguished newspapers recently. 
The radical economic growth and change in our society toward nuclear families exposed our elders to the challenges that were nonexistent in the 20th century.
 In old days, sons of the family were supposed to take care of their elders; however, fierce competition and transformation from an agrarian society to service-oriented society flushed the youth from their homes to the cities with better job opportunities in the fight to survive. Undoubtedly it presented the youth with a better quality of life and global opportunities but came harsh on the elderly population. The people who spent most of their lives in the joint families suddenly find themselves living alone in their old age, the time when they needed to be pampered the most.   People with strong friend circles and active social life survived this transformation but others became hapless and the feeling of loneliness started crawling in their lives which later resulted in depression</p>
<p>There may be a well-equipped healthcare infrastructure but there is a huge delivery gap in addressing the mental needs of the elderly population in India. India needs to develop an infrastructure to address the companionship needs of the elders living alone. We have to provide social groups where people can talk their heart out. It will relieve them from the emotional burden, which they often carry with them. 
 </p>
 
<h3>Domestic Help & Trust</h3>
<p>It is a well-accepted fact that nuclear families are the future of Indian society. The acceptance means that in future and to a great extent in present, most of the elders will depend on outside help for their daily needs. The needs as simple as making a cup of tea to tasks like buying vegetables from the market. The e-commerce market has tried to solve the shopping woes but it does not solve the problem of elders. Elderly people are not well versed with the technology and they still believe in the hand-on shopping experience. The biggest challenge here is trust gaps. Old people find it difficult to trust people. Even children living away from their parents find it difficult to trust people with the care of the elderly back home. And you can’t blame them for it; recent events in the cities where elderly have been taken advantage of are well highlighted by the media. There is a need for developing a trustworthy service that can help the elderly with their household chores.
 The people recruited in these services need to be patient, empathic and trustworthy and should be trained to develop the bond of care.  </p>
<h3>Travel and Transport</h3>
<p>Travelling and transport can be tedious in the old age. The economic development had ensured that every middle-class family owns a car. However, the biggest challenge elderly face with the traveling is the unavailability of the driver and a trustworthy companion to travel with. The elderly who don’t have a car find it difficult to travel through public transport and are often afraid of booking a car from unknown providers. The delivery gap means missing family functions, routine check-ups, and cut off from the social circle. In the times, when everyone has to be on their own, it is essential to have someone you can trust with your travel plans. </p>
<h3>Solutions in a Nutshell  </h3>
<p>There are many healthcare companies in India who understood these gaps and tried to the seal the gaps through a set of professional services. However, one point solutions remained elusive. The ivh SeniorCare is committed to sealing all the delivery gaps and providing a wide array of services that include healthcare and concierge services. The concierge services through ex-defence personnel are our biggest differentiation in the home care industry. Whether you need a driver or a physician, we are there for you because we understand that Eldercare is not about the health only, it’s about adding life to the healthy years. </p>
