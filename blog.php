							<h3>Blog Post </h3>
							<p class="project-dp-one">Recent projections for elderly populations have flooded the media with the stories on aging India.
                            Everyone is talking about how India is greying faster than expected and how will it affect the economic
                            growth plans. However, the point everyone missing is that aging India will need an extensive geriatric
                            healthcare system, the system that must be developed before the projections become reality.</p>
							<p class="project-dp-two">Aging is an unavoidable process that brings multiple changes with it. Except for the wisdom, every
                                                aspect of a person deteriorates with the age. Some changes like wrinkles, decreasing body, and digestive
                                                 strength are easily noticed whereas changes like decreased renal and cardiac efficiency, low bone mass
                                                 are only noticed in routine physical examinations. The tasks that were a routine suddenly start becoming
                                                   challenges.</p>
