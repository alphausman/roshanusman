						<p>Old age challenges were never a problem in India until the nation started its journey from its well-knit
                           joint family structure to the nuclear family structure. Additionally, rapid globalization and economic
                             growth presented the Indian youth with the opportunities that were out of reach in the past and
                              initiated an array of domestic and international migrations.</p>
							  <p>Senior care, a sacrament and representation of Indian values in the past, suddenly became a challenge
to the society, which was moving from east to west. Children were not available anymore to take care of
the elders of the family. The problem was more evident in high socioeconomic section as they became

the first prey of westernisation and frequent travelling was a part of their lifestyle. Everyone was coping
with the new problem in their own way. Low-income groups were taking the help of relatives and
middle and high-income groups were mostly dependent on the domestic help.</p>

<p>To address the challenge, a completely new healthcare segment came into existence. Senior care or
elder care services with their professional approach were a promising solution to the problem. India was
not only accepting the problems of westernisation but also harnessing the solution to those problems
from the west only.</p>

<p>Senior care solutions were there in the market but lacked an organized structure under a single roof. I
believe people failed to understand the fact that healthcare is not the eldercare; it is just a part of it.
Eldercare in itself is a multifaceted task that requires well organized multifaceted solutions. In addition
to healthcare, there was a need to address the elderly requirements like companionship, social
interaction, hobby activities, travel arrangements, and so on. Arranging the services under one roof is
not difficult, but making people believe in a service is.</p>
<p>IVH SeniorCare was born with the objective of addressing the eldercare challenges. Like-minded
professionals from diverse backgrounds (healthcare communication, defence forces, healthcare delivery
business) came together to develop a one-stop solution to cater the increasing geriatric population of
the country.</p>

<p>IVH Seniorcare replicated the proven family support system of armed forces to deliver its services
through care mangers, who are ex-defence personnel. The care managers are well versed with family
support system and bring in the trust, empathy, and dedication in the eldercare. After all, armed forces
are the solution for every problem in India, be it national defence, internal state problems, or
conducting a peaceful election in the largest democracy of the world. Recent Kerala floods also
highlighted the indispensable nature of our armed forces in the need of the hour.</p>

<p>Involving ex-defense personnel in the healthcare system was a move that killed two birds with one
stone. On the one hand, it enriched the senior care services with defence values of dedication, empathy
discipline and trust and on the other hand, it provided the ex-defence personnel with a reputed job
option after retirement.</p>

<p>India may not be at par with the standards of the developed world in the field of eldercare but has
definitely started to understand the challenge, and initiatives like IVH SeniorCare who envisioned
building an army of veterans to address the eldercare challenges is the proof that we are evolving and
we are evolving really fast.</p><h3>Commander Rishi Singh</h3>
